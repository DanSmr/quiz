public class Question {
    private String question;
    private String A;
    private String B;
    private String C;
    private String D;
    private String answer;

    // Getters and setters

    public String getQuestion() {
        return question;
    }

    public String getA() {
        return A;
    }

    public String getB() {
        return B;
    }

    public String getC() {
        return C;
    }

    public String getD() {
        return D;
    }

    public String getAnswer() {
        return answer;
    }



}



